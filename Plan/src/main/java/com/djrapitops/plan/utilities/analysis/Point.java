package main.java.com.djrapitops.plan.utilities.analysis;

/**
 *
 * @author Rsl1122
 * @since 3.5.2
 */
public class Point {
    private final double x;
    private final double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
